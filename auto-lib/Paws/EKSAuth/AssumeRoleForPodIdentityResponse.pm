=head1 NAME

Paws::EKSAuth::AssumeRoleForPodIdentityResponse

=head1 ATTRIBUTES


=head2 B<REQUIRED> AssumedRoleUser => L<Paws::EKSAuth::AssumedRoleUser>

An object with the permanent IAM role identity and the temporary
session name.

The ARN of the IAM role that the temporary credentials authenticate to.

The session name of the temporary session requested to STS. The value
is a unique identifier that contains the role ID, a colon (C<:>), and
the role session name of the role that is being assumed. The role ID is
generated by IAM when the role is created. The role session name part
of the value follows this format:
C<eks-I<clustername>-I<podname>-I<random UUID>>


=head2 B<REQUIRED> Audience => Str

The identity that is allowed to use the credentials. This value is
always C<pods.eks.amazonaws.com>.


=head2 B<REQUIRED> Credentials => L<Paws::EKSAuth::Credentials>

