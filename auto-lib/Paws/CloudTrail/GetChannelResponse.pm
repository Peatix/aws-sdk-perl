
package Paws::CloudTrail::GetChannelResponse;
  use Moose;
  has ChannelArn => (is => 'ro', isa => 'Str');
  has Destinations => (is => 'ro', isa => 'ArrayRef[Paws::CloudTrail::Destination]');
  has IngestionStatus => (is => 'ro', isa => 'Paws::CloudTrail::IngestionStatus');
  has Name => (is => 'ro', isa => 'Str');
  has Source => (is => 'ro', isa => 'Str');
  has SourceConfig => (is => 'ro', isa => 'Paws::CloudTrail::SourceConfig');

  has _request_id => (is => 'ro', isa => 'Str');

### main pod documentation begin ###

=head1 NAME

Paws::CloudTrail::GetChannelResponse

=head1 ATTRIBUTES


=head2 ChannelArn => Str

The ARN of an channel returned by a C<GetChannel> request.


=head2 Destinations => ArrayRef[L<Paws::CloudTrail::Destination>]

The destinations for the channel. For channels created for
integrations, the destinations are the event data stores that log
events arriving through the channel. For service-linked channels, the
destination is the Amazon Web Services service that created the
service-linked channel to receive events.


=head2 IngestionStatus => L<Paws::CloudTrail::IngestionStatus>

A table showing information about the most recent successful and failed
attempts to ingest events.


=head2 Name => Str

The name of the CloudTrail channel. For service-linked channels, the
name is C<aws-service-channel/service-name/custom-suffix> where
C<service-name> represents the name of the Amazon Web Services service
that created the channel and C<custom-suffix> represents the suffix
generated by the Amazon Web Services service.


=head2 Source => Str

The source for the CloudTrail channel.


=head2 SourceConfig => L<Paws::CloudTrail::SourceConfig>

Provides information about the advanced event selectors configured for
the channel, and whether the channel applies to all Regions or a single
Region.


=head2 _request_id => Str


=cut

1;